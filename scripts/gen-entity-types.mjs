// Connects to your Home Assistant instance and writes the list of real entity
// IDs into src/hass/entities.generated.ts, giving you autocomplete inside
// useEntity('...'). Run with: npm run gen:types
//
// Reads VITE_HASS_URL / VITE_HASS_TOKEN from .env.local.

import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  createConnection,
  createLongLivedTokenAuth,
  getStates,
} from 'home-assistant-js-websocket';
import WebSocket from 'ws';

// home-assistant-js-websocket expects a global WebSocket (browser API).
globalThis.WebSocket = WebSocket;

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');

// Minimal .env.local parser (avoids an extra dependency).
function loadEnv() {
  const env = {};
  const envPath = path.join(root, '.env.local');
  if (existsSync(envPath)) {
    for (const line of readFileSync(envPath, 'utf8').split('\n')) {
      const m = line.match(/^\s*([\w.-]+)\s*=\s*(.*)\s*$/);
      if (m && !line.trim().startsWith('#')) {
        env[m[1]] = m[2].replace(/^["']|["']$/g, '').trim();
      }
    }
  }
  return env;
}

const env = loadEnv();
const hassUrl = env.VITE_HASS_URL || process.env.VITE_HASS_URL;
const token = env.VITE_HASS_TOKEN || process.env.VITE_HASS_TOKEN;

if (!hassUrl || !token) {
  console.error(
    'Bitte VITE_HASS_URL und VITE_HASS_TOKEN in .env.local setzen.',
  );
  process.exit(1);
}

const auth = createLongLivedTokenAuth(hassUrl, token);
const connection = await createConnection({ auth });
const states = await getStates(connection);
const ids = states.map((s) => s.entity_id).sort();

const union = ids.map((id) => `  | '${id}'`).join('\n');
const out =
  `// AUTO-GENERATED by \`npm run gen:types\`. Do not edit by hand.\n` +
  `// ${ids.length} entities from ${hassUrl}\n` +
  `export type KnownEntityId =\n${union}\n  | (string & {});\n`;

writeFileSync(path.join(root, 'src', 'hass', 'entities.generated.ts'), out);
console.log(`✓ ${ids.length} Entities → src/hass/entities.generated.ts`);

connection.close();
process.exit(0);
